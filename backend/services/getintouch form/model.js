const mongoose = require("mongoose");

const ContactFormSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
      lowercase: true,
    },
    subject: {
      type: String,
      required: true,
    },
    message: {
      type: String,
      required: true,
    },
    createdAt: {
      type: Date,
      default: Date.now,
    },
  },
  { timestamps: true }
);

const ContactForm = mongoose.model(
  "ContactForm",
  ContactFormSchema,
  "ContactForms"
);

module.exports = { ContactForm };
